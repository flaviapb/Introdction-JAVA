package loopdowhile;

public class EntendendoEsseLoop {

	public static void main(String[] args) {
		
		int num=1;
		int num2=1;
		
		do {
			System.out.println(num);
			num++;
		} while (num < 1);
		
		System.out.print("---------------------------------------------------------------------");
		
		while(num2 < 1) {
			
			System.out.println(num2);
			num2++;			
		}

	}

}
